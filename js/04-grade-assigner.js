let gradeAssigner = Number(prompt('Enter a number between 1 and 100 (ONLY numbers between 1-100 are accepted'));

if (gradeAssigner >= 90) {
    alert('You received an A');
    } else if (gradeAssigner >= 80) {
    alert('You received a B');
    } else if (gradeAssigner >= 70) {
    alert('You received a C');
    } else if (gradeAssigner >= 60) {
    alert('You received a D');
    } else {
    alert('You received an F');
} 